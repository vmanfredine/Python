# Adição
adicao = 2 + 2
print(adicao)

# Subtração
subtracao = 7 - 1
print(subtracao)

# Multiplicação
multiplicacao = 8 * 2
print(multiplicacao)

# Divisão
divisao = 13/8
print(divisao)

# Divisão de inteiros/floored quotient
divisao_inteiros = 13 // 8
print(divisao_inteiros)

# Módulo/resto - Retorna o resto da divisão de a por b
modulo = 14 % 4
print(modulo)

# Expoente
expoente = 2 ** 2
print(expoente)
