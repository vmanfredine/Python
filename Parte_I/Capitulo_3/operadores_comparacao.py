# Maior que
maior_que = 100 > 10
print(maior_que)

# Menor que
menor_que = 100 < 10
print(menor_que)

# Maior ou igual a
maior_igual = 2 >= 2
print(maior_igual)

# Menor ou igual a
menor_igual = 1 <= 4
print(menor_igual)

# Igual
igual = 6 == 9
print(igual)

# Diferente
diferente = 3 != 2
print(diferente)